type TranslationKey = keyof typeof zh;

interface I18nContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey, vars?: Record<string, string | number>) => string;
  providerLabel: (provider: Provider) => string;
  resultLabel: (result: BalanceResult | null) => string;
  eventLabel: (event: NotificationEventType) => string;
  proxyLabel: (mode: ProxyMode) => string;
  scheduleStatusLabel: (status: ScheduleStatus) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

function detectLocale(): Locale {
  const stored = localStorage.getItem(STORAGE_KEY);
  if (stored === "zh-CN" || stored === "en-US") return stored;
  return navigator.language.toLowerCase().startsWith("zh") ? "zh-CN" : "en-US";
}

function interpolate(template: string, vars?: Record<string, string | number>) {
  if (!vars) return template;
  return Object.entries(vars).reduce(
    (text, [key, value]) => text.split(`{{${key}}}`).join(String(value)),
    template,
  );
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => detectLocale());

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, locale);
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => {
    const dict = locale === "zh-CN" ? zh : en;
    const t = (key: TranslationKey, vars?: Record<string, string | number>) =>
      interpolate(dict[key] ?? en[key] ?? key, vars);
    return {
      locale,
      setLocale: setLocaleState,
      t,
      providerLabel: (provider) => t(`provider.${provider}` as TranslationKey),
      resultLabel: (result) =>
        result == null
          ? t("result.unchecked")
          : t(`result.${result}` as TranslationKey),
      eventLabel: (event) => t(`event.${event}` as TranslationKey),
      proxyLabel: (mode) => t(`proxy.${mode}` as TranslationKey),
      scheduleStatusLabel: (status) =>
        t(`scheduleStatus.${status}` as TranslationKey),
    };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return value;
}
